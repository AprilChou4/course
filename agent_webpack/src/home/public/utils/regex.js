/**
 * @func 正则表达式
 */
const regex = {
  // 手机
  // mobile:/^0?(13|14|15|17|18)[0-9]{9}$/,
  mobile: /^1\d{10}$/,
  // 电话
  tel: /^[0-9-()（）]{7,18}$/,
  // 邮箱
  email: /^\w+((-w+)|(\.\w+))*\@[A-Za-z0-9]+((\.|-)[A-Za-z0-9]+)*\.[A-Za-z0-9]+$/,
  // 身份证
  // 15位或者18位，15位时全为数字，18位前17位为数字，最后一位是校验位，可能为数字或字符X
  idcard: /(^\d{15}$)|(^\d{17}(\d|X|x)$)/,
  // 中文
  cn: /^[\u4e00-\u9fa5]+$/,
  // 税号
  taxnum: /^[a-zA-Z0-9]{15,20}$/,
  // excel文件
  excel: /^.+\.(xls|xlsx)$/,
};

export default regex;
